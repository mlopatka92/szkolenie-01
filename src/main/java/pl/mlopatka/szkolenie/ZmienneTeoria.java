package pl.mlopatka.szkolenie;

public class ZmienneTeoria {

    // To jest komentarz, można tu wpisać dowolną treść i nie wpłynie ona na działanie programu

    // To jest stała, jej wartość nigdy nie zostanie zmieniona.
    // Każda stała jest składa się ze słów kluczowych "static final", typu a także nazwy
    // Konwencja nazewnictwa stałej: stała powinna być rzeczownikiem, same duże litery, słowa oddzielone znakiem: "_"
    private static final int WARTOSC_STALA = 12;

    public static void main(String[] args) {

        // Deklaracja zmiennej: typ + nazwa zmiennej
        // nazwa zmiennej powinna być rzeczownikiem, camel case (pierwsza litera z małej, kolejne wrazy z dużych liter
        long nazwaZmiennej;

        // Inicjalizacja zmiennej (przypisanie do zmiennej wartości / literału) - w tym przypadku przypisujemy do zmiennej
        // literał typu long.
        nazwaZmiennej = 1221421412414221214L;

        // Deklaracja zmiennej (deklaracja + inicjalizacja)
        int zadeklarowanaZmienna = 12;

    }
}
